//null vs undefined
// undefined: a value exists, but no value has been assigned to it yet.javascript itself set it automatically.
//  It is the default value of uninitialized variables. eg: 
var x;
console.log(x); //undefined
// null: a value exists, but it is explicitly set to have no value or empty . it is intentional absence of any value.
// . It is an assignment value that can be assigned to a variable as a representation of no value.
var audi=null;
console.log(audi); //null
let profilePicture=null;
console.log(profilePicture); //null 
console.log(typeof profilePicture); //object