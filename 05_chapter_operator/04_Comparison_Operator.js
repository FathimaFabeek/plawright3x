//Comparison operator
//==,===,
//==:-loose comparison operator, checks only value
//===:-strict comparison operator, checks both value and type
console.log(3<4);
console.log(3>4);
console.log(4>=4);
console.log(4<=4);
console.log(4==4);//loose comparison operator, or
console.log(4=="4");//loose comparison operator
console.log(4==="4");//strict comparison operator, and
console.log(4!=4);//loose comparison operator
console.log(4!=="4");//strict comparison operator